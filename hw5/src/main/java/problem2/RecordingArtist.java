package problem2;

/**
 * Class RecordingArtist represents a recording artist for a piece of Music as their first and
 * last names.
 */

public class RecordingArtist extends Individual {

  /**
   * Constructor for creating a new RecordingArtist with the given first and last names.
   * @param firstName - The first name to be set, as a String.
   * @param lastName - The last name to be set, as a String.
   */
  public RecordingArtist(String firstName, String lastName) {
    super(firstName, lastName);
  }
}
